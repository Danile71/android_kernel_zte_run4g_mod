#ifndef _MTK_SYNCAE_ERRCODE_H
#define _MTK_SYNCAE_ERRCODE_H


///////////////////////////////////////////////////////////////////////////
//! Error code type definition
///////////////////////////////////////////////////////////////////////////
typedef MINT32 MRESULT;

///////////////////////////////////////////////////////////////////////////
//! Helper macros to define error code
///////////////////////////////////////////////////////////////////////////
#define ERRCODE(modid, errid)           \
  ((MINT32)                              \
    ((MUINT32)(0x80000000) |             \
     (MUINT32)((modid & 0x7f) << 24) |   \
     (MUINT32)(errid & 0xffff))          \
  )

#define OKCODE(modid, okid)             \
  ((MINT32)                              \
    ((MUINT32)(0x00000000) |             \
     (MUINT32)((modid & 0x7f) << 24) |   \
     (MUINT32)(okid & 0xffff))           \
  )

///////////////////////////////////////////////////////////////////////////
//! Helper macros to check error code
///////////////////////////////////////////////////////////////////////////
#define SUCCEEDED(Status)   ((MRESULT)(Status) >= 0)
#define FAILED(Status)      ((MRESULT)(Status) < 0)

#define MODULE_MTK_SYNCAE (5) // Temp value

#define MTKSYNCAE_OKCODE(errid)         OKCODE(MODULE_MTK_SYNCAE, errid)
#define MTKSYNCAE_ERRCODE(errid)        ERRCODE(MODULE_MTK_SYNCAE, errid)


//Error code
#define S_SYNCAE_OK                  MTKSYNCAE_OKCODE(0x0000)

#define E_SYNCAE_NEED_OVER_WRITE     MTKSYNCAE_ERRCODE(0x0001)
#define E_SYNCAE_NULL_OBJECT         MTKSYNCAE_ERRCODE(0x0002)
#define E_SYNCAE_WRONG_STATE         MTKSYNCAE_ERRCODE(0x0003)
#define E_SYNCAE_WRONG_CMD_ID        MTKSYNCAE_ERRCODE(0x0004)
#define E_SYNCAE_WRONG_CMD_PARAM     MTKSYNCAE_ERRCODE(0x0005)
#define E_SYNCAE_NOT_ENOUGH_MEM      MTKSYNCAE_ERRCODE(0x0006)
#define E_SYNCAE_NULL_INIT_PARAM     MTKSYNCAE_ERRCODE(0x0007)


#define E_SYNCAE_ERR                 MTKSYNCAE_ERRCODE(0x0100)

#endif

